                <h5 class="card-detail-title text-center my-5">torréfaction des grains de café</h5>
                <p class="card-detail-text">
            
                        Une fois que les grains de café prennent la couleur verte, elles subissent une torréfaction . <br>
                    <b> La torréfaction</b> est un processus thermique qui transforme le café en grains brun foncé aromatiques prêts à être broyés.
                        La torréfaction accentue l’arôme et la saveur cachés dans les grains de café vert. <br> <br> En général, les grains de café sont stockés
                        dans leur état vert, un état qui peut être maintenu sans compromettre la qualité ou le goût. Les grains verts n’ont pas les attributs des grains de café torréfiés
                        – ils sont mous, spongieux et dégagent un parfum d’herbes. <br> <br> La torréfaction entraîne des changements chimiques importants. Les grains sont rapidement exposés à des températures
                        excessivement élevées. Après avoir atteint le niveau de torréfaction désiré, ils sont rapidement refroidis pour arrêter le processus. L’arôme des grains torréfiés ressemble à celui du café,
                        et ils pèsent moins que leurs homologues verts en raison de la perte d’humidité pendant la torréfaction. Ils deviennent croustillants et sont prêts pour le broyage et la fermentation.<br> <br>
                        Les degrés de torréfaction varient en fonction de la température et de la durée de torréfaction. Les grains de café torréfiés foncés subissent une torréfaction plus longue, ce qui donne une teinte
                        plus profonde et un goût torréfié plus prononcé. Inversement, la torréfaction prolongée diminue les saveurs vibrantes et réduit les niveaux de caféine.
                </p>

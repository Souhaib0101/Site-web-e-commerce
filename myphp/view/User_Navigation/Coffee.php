                <h5 class="card-detail-title text-center my-5">Dépulpage des cerises de café</h5>
                <p class="card-detail-text">
            



                    Après la récolte des cerises de café mûres, l’étape suivante consiste à retirer la peau extérieure de la cerise pour exposer les grains de café verts à l’intérieur. 
                    Il existe plusieurs méthodes de dépulpage : <br> <br>

                <b>  Dépulpage humide (traitement lavé) : </b>     Dans cette méthode, les cerises sont immergées dans de l’eau pour adoucir la peau et la pulpe.
                     Les cerises sont ensuite alimentées par une machine à dépulper qui élimine mécaniquement la peau et la pulpe. Après le dépulpage, 
                     les grains de café vert sont soigneusement lavés pour éliminer tout résidu. <br> <br>

                <b>  Dépulpage à sec (traitement naturel) : </b>  Dans ce processus, les cerises sont simplement étalées au soleil pour sécher pendant plusieurs semaines. 
                     Pendant le séchage, la peau et la pulpe se dessèchent et rétrécissent, ce qui facilite le retrait manuel.

                </p>
